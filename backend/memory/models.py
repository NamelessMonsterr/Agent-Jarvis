from pydantic import BaseModel, Field
from typing import Optional, Literal
from datetime import datetime, date
from uuid import UUID, uuid4
import hashlib


class Message(BaseModel):
    role: Literal["user", "assistant", "system", "agent"]
    content: str
    timestamp: datetime = Field(default_factory=datetime.utcnow)
    agent_name: Optional[str] = None

    def to_llm_dict(self) -> dict:
        role = "assistant" if self.role == "agent" else self.role
        return {"role": role, "content": self.content}


class Session(BaseModel):
    id: UUID = Field(default_factory=uuid4)
    user_id: str
    messages: list[Message] = Field(default_factory=list)
    active_agents: list[str] = Field(default_factory=list)
    metadata: dict = Field(default_factory=dict)
    created_at: datetime = Field(default_factory=datetime.utcnow)
    closed_at: Optional[datetime] = None


class Paper(BaseModel):
    title: str
    abstract: str = ""
    authors: list[str] = Field(default_factory=list)
    year: Optional[int] = None
    source: Literal["arxiv", "semantic_scholar", "unknown"] = "unknown"
    url: str = ""
    doi: Optional[str] = None
    citation_count: int = 0

    def content_hash(self) -> str:
        key = self.doi if self.doi else self.title.lower().strip()
        return hashlib.sha256(key.encode()).hexdigest()[:16]

    def to_context_str(self) -> str:
        authors_str = ", ".join(self.authors[:3]) + (" et al." if len(self.authors) > 3 else "")
        return (
            f"**{self.title}** ({self.year or '?'}) — {authors_str}\n"
            f"{self.abstract[:300]}...\n[{self.source}] {self.url}"
        )


