class CommitMetadata(BaseModel):
    commit_sha: str
    summary: str
    timestamp: datetime = Field(default_factory=datetime.utcnow)
    branch: str = "jarvis/devlog"
    repo: str = ""
    url: str = ""


class Devlog(BaseModel):
    id: Optional[UUID] = None
    user_id: str
    date: date
    summary: str
    tasks: list[Task] = Field(default_factory=list)
    commits: list[str] = Field(default_factory=list)
    commit_metadata: list[CommitMetadata] = Field(default_factory=list)
    topics: list[str] = Field(default_factory=list)
    mood: Optional[int] = Field(None, ge=1, le=5)
    github_path: Optional[str] = None
    created_at: Optional[datetime] = None
    updated_at: Optional[datetime] = None

    def to_markdown(self) -> str:
        lines = [f"# Dev Log — {self.date.strftime('%B %d, %Y')}", "", "## Summary", self.summary, ""]
        if self.tasks:
            lines += ["## Tasks", ""]
            icons = {"done": "✅", "in_progress": "🔄", "blocked": "🚫", "planned": "📋"}
            for t in self.tasks:
                note = f" — {t.notes}" if t.notes else ""
                lines.append(f"- {icons.get(t.status, '•')} {t.task}{note}")
            lines.append("")
        if self.topics:
            lines += ["## Topics", ", ".join(self.topics), ""]
        if self.commits:
            lines += ["## Commits", ""]
            for sha in self.commits:
                lines.append(f"- `{sha}`")
            lines.append("")
        if self.mood:
            lines += [f"## Mood", f"{'⭐' * self.mood} ({self.mood}/5)", ""]
        lines += ["---", f"*Generated by Jarvis on {datetime.utcnow().strftime('%Y-%m-%d %H:%M UTC')}*"]
        return "\n".join(lines)


